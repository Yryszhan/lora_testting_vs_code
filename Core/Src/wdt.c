/**
  * @file    wdt.c
  * @brief   Независимый сторожевой таймер (IWDG) для STM32F103.
  *
  *  IWDG работает от собственного генератора LSI (~40 кГц), не зависит
  *  от основного клока. Если программа зависнет и не вызовет wdt_kick()
  *  в течение таймаута — IWDG перезагрузит МК. Это защита удалённого
  *  узла от зависаний (никто не нажмёт reset на качалке).
  *
  *  Расчёт таймаута:
  *    T = (4 * 2^PR) * (RLR + 1) / F_lsi
  *    F_lsi ≈ 40000 Гц
  *  При PR=6 (делитель 256) и RLR=4095:
  *    T = (4 * 256) * 4096 / 40000 ≈ 26.2 секунды
  *
  *  Узел шлёт раз в минуту, но wdt_kick() нужно вызывать ЧАЩЕ таймаута.
  *  При интервале передачи 60 с и таймауте 26 с — добавь промежуточные
  *  kick внутри ожидания (см. пример в main).
  */
#include "main.h"

static IWDG_HandleTypeDef hiwdg;

/**
  * @brief Инициализация IWDG с таймаутом ~26 секунд.
  *        Вызывать ОДИН раз при старте, после инициализации периферии.
  *        После запуска IWDG нельзя остановить (только сброс МК).
  */
void wdt_init(void)
{
    hiwdg.Instance       = IWDG;
    hiwdg.Init.Prescaler = IWDG_PRESCALER_256;  /* делитель 256       */
    hiwdg.Init.Reload    = 4095;                 /* макс. перезагрузка */
    /* (4*256)*4096/40000 ≈ 26.2 c */
    HAL_IWDG_Init(&hiwdg);
}

/**
  * @brief Сбросить watchdog ("погладить собаку").
  *        Вызывать в основном цикле ЧАЩЕ, чем таймаут (< 26 c).
  */
void wdt_kick(void)
{
    HAL_IWDG_Refresh(&hiwdg);
}

/**
  * @brief Задержка с периодическим сбросом watchdog.
  *        Заменяет длинный HAL_Delay, чтобы не словить сброс при ожидании.
  * @param total_ms полное время ожидания в мс
  */
void wdt_delay(uint32_t total_ms)
{
    const uint32_t step = 5000;  /* гладим каждые 5 c — с запасом < 26 c */
    while (total_ms > step) {
        HAL_Delay(step);
        wdt_kick();
        total_ms -= step;
    }
    HAL_Delay(total_ms);
    wdt_kick();
}